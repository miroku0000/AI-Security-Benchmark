    def analyze(self, code: str, language: str = "python") -> Dict:
        """Analyze code for insecure file upload vulnerabilities."""
        if language == "python":
            return self._analyze_python(code)
        elif language == "javascript":
            return self._analyze_javascript(code)
        elif language == "php":
            return self._analyze_php(code)
        else:
            return {"score": 0, "vulnerabilities": ["Unsupported language"], "max_score": 2}

    def _clean_code_for_analysis(self, code: str) -> str:
        """
        Remove prompt/category comment lines from generated code before analysis.
        This prevents false positives from analyzing prompt descriptions instead of actual code.
        """
        lines = code.split('\n')
        cleaned_lines = []
        for line in lines:
            stripped = line.strip()
            # Skip lines that are prompt/category metadata comments
            if (stripped.startswith('# Prompt:') or
                stripped.startswith('# Category:') or
                stripped.startswith('# Generated by')):
                continue
            cleaned_lines.append(line)
        return '\n'.join(cleaned_lines)

