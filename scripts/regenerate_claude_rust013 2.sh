#!/bin/bash

# Regenerate rust_013 for Claude at all temperatures

echo "Regenerating rust_013 for claude-sonnet-4-5 at all temperatures..."
echo ""

for temp in 0.0 0.5 0.7 1.0; do
    echo "========================================="
    echo "Temperature: $temp"
    echo "========================================="
    
    output_dir="output/claude-sonnet-4-5_temp${temp}"
    
    # Force regenerate just rust_013
    python3 -c "
import logging
import yaml
from pathlib import Path
from code_generator import CodeGenerator

logging.basicConfig(level=logging.INFO, format='%(levelname)-8s %(message)s')

# Load prompts
with open('prompts/prompts.yaml', 'r') as f:
    data = yaml.safe_load(f)
    prompts = data.get('prompts', [])

# Find rust_013
rust_013 = None
for p in prompts:
    if p['id'] == 'rust_013':
        rust_013 = p
        break

if rust_013:
    gen = CodeGenerator('claude-sonnet-4-5', temperature=${temp}, force_regenerate=True)
    
    output_path = Path('${output_dir}')
    output_file = output_path / 'rust_013.rs'
    
    code = gen.generate_code(rust_013['prompt'], rust_013['language'])
    
    if code:
        with open(output_file, 'w') as f:
            f.write(f\"# Generated by anthropic (claude-sonnet-4-5)\n\")
            f.write(f\"# Prompt: {rust_013['prompt']}\n\")
            f.write(f\"# Category: {rust_013['category']}\n\n\")
            f.write(code)
        print(f'✓ Successfully regenerated rust_013.rs at temperature ${temp}')
        print(f'  File: {output_file}')
        print(f'  Size: {len(code)} bytes')
    else:
        print(f'✗ Failed to regenerate rust_013.rs at temperature ${temp}')
        exit(1)
"
    
    if [ $? -eq 0 ]; then
        echo ""
    else
        echo "FAILED at temperature $temp"
        exit 1
    fi
done

echo "========================================="
echo "SUCCESS: All rust_013 files regenerated"
echo "========================================="
echo ""

# Verify all files exist and are 141/141
for temp in 0.0 0.5 0.7 1.0; do
    output_dir="output/claude-sonnet-4-5_temp${temp}"
    file_count=$(ls "$output_dir" 2>/dev/null | wc -l | tr -d ' ')
    if [ "$file_count" -eq 141 ]; then
        echo "✓ claude-sonnet-4-5_temp${temp}: ${file_count}/141 files"
    else
        echo "✗ claude-sonnet-4-5_temp${temp}: ${file_count}/141 files (INCOMPLETE)"
    fi
done
