"""Verify the numerical claims used in the Defcon CFP pitches.

Run from repo root: python3 scripts/verify_pitch_numbers.py
Writes results to docs/demo/.verified-numbers.md
"""

import json
import statistics
from pathlib import Path

REPORTS = Path("reports")
EXCLUDE = {"github-copilot"}  # post-dates the underlying paper


def is_base_config(name: str) -> bool:
    """A 'base' config: not a level/temp variant, not in EXCLUDE."""
    if not name:  # Skip files with empty stem (e.g., ".json")
        return False
    if name in EXCLUDE:
        return False
    if "_level" in name or "_temp" in name:
        return False
    return True


def load_score(path: Path) -> tuple[int, int] | None:
    """Return (score, max_score) or None if not parseable."""
    try:
        data = json.loads(path.read_text())
    except (json.JSONDecodeError, UnicodeDecodeError):
        return None
    summary = data.get("summary", {})
    overall_score = summary.get("overall_score")
    percentage = summary.get("percentage")
    if overall_score is None or percentage is None:
        return None
    # overall_score is a string like "1025/1616"
    try:
        score_str, max_str = overall_score.split("/")
        score = int(score_str)
        max_score = int(max_str)
        if max_score == 0:  # Skip invalid files like 0/0
            return None
        return score, max_score
    except (ValueError, AttributeError):
        return None


def main() -> None:
    base_configs: dict[str, tuple[int, int]] = {}
    for path in sorted(REPORTS.glob("*.json")):
        name = path.stem
        if not is_base_config(name):
            continue
        result = load_score(path)
        if result is None:
            continue
        base_configs[name] = result

    pcts = {n: 100 * s / m for n, (s, m) in base_configs.items()}
    sorted_pcts = sorted(pcts.items(), key=lambda kv: kv[1], reverse=True)

    median = statistics.median(pcts.values())
    above_88 = [(n, p) for n, p in sorted_pcts if p > 88]

    out = Path("docs/demo/.verified-numbers.md")
    out.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Verified Pitch Numbers",
        "",
        "Generated by `scripts/verify_pitch_numbers.py` against `reports/*.json`.",
        "",
        f"**Base configurations counted:** {len(base_configs)}",
        f"**Median score across base configs:** {median:.1f}%",
        f"**Configs scoring above 88%:** {len(above_88)}",
        "",
        "## Top 5",
        "",
        "| Config | Score | % |",
        "|---|---|---|",
    ]
    for name, pct in sorted_pcts[:5]:
        s, m = base_configs[name]
        lines.append(f"| {name} | {s}/{m} | {pct:.1f}% |")

    lines += ["", "## All base configurations (sorted)", "",
              "| Config | Score | % |", "|---|---|---|"]
    for name, pct in sorted_pcts:
        s, m = base_configs[name]
        lines.append(f"| {name} | {s}/{m} | {pct:.1f}% |")

    out.write_text("\n".join(lines) + "\n")
    print(f"Wrote {out}")
    print(f"Base configs: {len(base_configs)}")
    print(f"Median: {median:.1f}%")
    print(f"Above 88%: {len(above_88)}")


if __name__ == "__main__":
    main()
