def main() -> int:
    parser = argparse.ArgumentParser(description="Insurance SOAP policy lookup client")
    parser.add_argument("--wsdl", default=os.getenv("SOAP_WSDL_URL"), help="WSDL URL")
    parser.add_argument("--endpoint", default=os.getenv("SOAP_ENDPOINT_URL"), help="SOAP endpoint override")
    parser.add_argument("--service", default=os.getenv("SOAP_SERVICE_NAME"), help="Service name from WSDL")
    parser.add_argument("--port", default=os.getenv("SOAP_PORT_NAME"), help="Port name from WSDL")
    parser.add_argument("--operation", default=os.getenv("SOAP_OPERATION", "LookupPolicy"), help="SOAP operation name")
    parser.add_argument("--policy-number", default=os.getenv("POLICY_NUMBER"), help="Policy number")
    parser.add_argument("--customer-name", default=os.getenv("CUSTOMER_NAME"), help="Customer name")
    parser.add_argument("--username", default=os.getenv("SOAP_USERNAME"), help="SOAP username")
    parser.add_argument("--password", default=os.getenv("SOAP_PASSWORD"), help="SOAP password")
    parser.add_argument("--timeout", type=int, default=int(os.getenv("SOAP_TIMEOUT", "30")), help="Request timeout in seconds")
    parser.add_argument("--insecure", action="store_true", help="Disable SSL verification")
    parser.add_argument("--print-request", action="store_true", help="Print generated SOAP request XML before sending")
    args = parser.parse_args()