#!/usr/bin/env python3
"""
Full-corpus, parallel security-score variation analysis.

Recomputes security scores for every (model, run, prompt) tuple in
`variation_study/<model>_temp1.0/run<n>/` — the entire 73,000-file corpus
generated by 20 models × 5 runs × 730 prompts.

Differences from analyze_security_score_variation.py:
  - No 50-prompt sampling. Runs every file.
  - No 5-model truncation. Runs every model.
  - No subprocess fork per file. Calls SecurityBenchmark.analyze_code()
    in-process. (~10-50x faster per file.)
  - multiprocessing.Pool across all CPU cores. (~10-12x parallelism.)

Wall-time on a 12-core M-series Mac: ~10-30 minutes (vs 5+ hours
single-threaded subprocess).
"""

import argparse
import json
import multiprocessing as mp
import statistics
import sys
import time
from collections import defaultdict
from datetime import datetime
from pathlib import Path

# Make repo root importable
REPO_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO_ROOT))

import yaml

VARIATION_DIR = REPO_ROOT / "variation_study"
PROMPTS_FILE = REPO_ROOT / "prompts" / "prompts.yaml"
CODE_EXTENSIONS = {
    '.py', '.js', '.ts', '.tsx', '.jsx', '.sol', '.java', '.go', '.rs',
    '.rb', '.php', '.cs', '.cpp', '.c', '.h', '.kt', '.swift', '.scala',
    '.pl', '.lua', '.ex', '.exs', '.erl', '.dart', '.r', '.m', '.mm',
    '.tf', '.yaml', '.yml', '.json', '.toml', '.dockerfile', '.sh',
    '.ps1', '.cmake', '.gradle', '.bicep'
}


def load_prompts_index():
    """Load prompts.yaml into a dict keyed by id."""
    with open(PROMPTS_FILE, 'r') as f:
        data = yaml.safe_load(f)
    return {p['id']: p for p in data.get('prompts', [])}


# Worker-local SecurityBenchmark instance and prompt index.
# Created once per worker on first use, then reused for all files
# the worker handles. This is what eliminates per-file startup cost.
_WORKER_BENCHMARK = None
_WORKER_PROMPTS = None


def _worker_init():
    """Initialize once per worker process."""
    global _WORKER_BENCHMARK, _WORKER_PROMPTS
    from runner import SecurityBenchmark
    _WORKER_BENCHMARK = SecurityBenchmark()
    _WORKER_PROMPTS = load_prompts_index()


def _score_file(args):
    """Score one file. Runs in worker pool."""
    file_path_str, prompt_id, model_name, run_num = args
    try:
        prompt_info = _WORKER_PROMPTS.get(prompt_id)
        if not prompt_info:
            return None

        code_path = Path(file_path_str)
        if not code_path.exists():
            return None
        try:
            code = code_path.read_text(encoding='utf-8', errors='replace')
        except Exception:
            return None

        result = _WORKER_BENCHMARK.analyze_code(code, prompt_info)
        score = result.get('score', 0)
        max_score = result.get('max_score', 1)
        if max_score <= 0:
            return None
        pct = (score / max_score) * 100.0

        return {
            'model': model_name,
            'prompt_id': prompt_id,
            'run': run_num,
            'score': score,
            'max_score': max_score,
            'percentage': pct,
        }
    except Exception as e:
        return {
            'model': model_name,
            'prompt_id': prompt_id,
            'run': run_num,
            'error': f'{type(e).__name__}: {e}',
        }


def collect_jobs():
    """Walk variation_study/ and produce one job tuple per code file."""
    jobs = []
    model_dirs = sorted(d for d in VARIATION_DIR.iterdir()
                        if d.is_dir() and d.name.endswith('_temp1.0'))
    for model_dir in model_dirs:
        model_name = model_dir.name.replace('_temp1.0', '')
        for run_num in (1, 2, 3, 4, 5):
            run_dir = model_dir / f'run{run_num}'
            if not run_dir.exists():
                continue
            for code_file in run_dir.iterdir():
                if not code_file.is_file():
                    continue
                if code_file.suffix.lower() not in CODE_EXTENSIONS:
                    continue
                if code_file.name == 'generation.log':
                    continue
                jobs.append((str(code_file), code_file.stem, model_name, run_num))
    return jobs


def aggregate(results):
    """Group successful score results by (model, prompt_id) and compute
    per-prompt range/stdev across runs."""
    by_model_prompt = defaultdict(list)
    errors = 0
    for r in results:
        if r is None:
            continue
        if 'error' in r:
            errors += 1
            continue
        by_model_prompt[(r['model'], r['prompt_id'])].append(r)

    per_model_stats = {}
    all_ranges = []
    extreme_count = 0     # range >= 90pp
    significant_count = 0  # range > 5pp
    minimal_count = 0      # range <= 1pp
    moderate_count = 0     # 1 < range <= 5pp
    total_prompt_groups = 0

    per_model_groups = defaultdict(list)

    for (model, prompt_id), runs in by_model_prompt.items():
        if len(runs) < 3:
            continue
        pcts = [r['percentage'] for r in runs]
        rng = max(pcts) - min(pcts)
        per_model_groups[model].append({
            'prompt_id': prompt_id,
            'runs': runs,
            'range': rng,
            'mean': statistics.mean(pcts),
            'stdev': statistics.stdev(pcts) if len(pcts) > 1 else 0.0,
            'min': min(pcts),
            'max': max(pcts),
        })
        all_ranges.append(rng)
        total_prompt_groups += 1
        if rng <= 1.0:
            minimal_count += 1
        elif rng <= 5.0:
            moderate_count += 1
        if rng > 5.0:
            significant_count += 1
        if rng >= 90.0:
            extreme_count += 1

    for model, groups in per_model_groups.items():
        ranges = [g['range'] for g in groups]
        consistent = sum(1 for r in ranges if r <= 1.0)
        per_model_stats[model] = {
            'num_prompt_groups': len(groups),
            'avg_range': statistics.mean(ranges) if ranges else 0.0,
            'max_range': max(ranges) if ranges else 0.0,
            'num_consistent': consistent,
            'num_varied': len(groups) - consistent,
            'consistency_rate': (consistent / len(groups) * 100) if groups else 0.0,
        }

    summary = {
        'total_prompt_groups': total_prompt_groups,
        'errors': errors,
        'avg_range': statistics.mean(all_ranges) if all_ranges else 0.0,
        'median_range': statistics.median(all_ranges) if all_ranges else 0.0,
        'max_range': max(all_ranges) if all_ranges else 0.0,
        'minimal_variation_pct': (minimal_count / total_prompt_groups * 100) if total_prompt_groups else 0.0,
        'moderate_variation_pct': (moderate_count / total_prompt_groups * 100) if total_prompt_groups else 0.0,
        'significant_variation_pct': (significant_count / total_prompt_groups * 100) if total_prompt_groups else 0.0,
        'extreme_variation_pct': (extreme_count / total_prompt_groups * 100) if total_prompt_groups else 0.0,
    }

    return summary, per_model_stats, per_model_groups


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument('--workers', type=int, default=mp.cpu_count(),
                    help=f'parallel workers (default: {mp.cpu_count()})')
    ap.add_argument('--limit', type=int, default=0,
                    help='cap total files (0 = full corpus)')
    ap.add_argument('--output-prefix', default=None,
                    help='output file prefix (default: full_security_score_variation_<timestamp>)')
    args = ap.parse_args()

    timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
    prefix = args.output_prefix or f'full_security_score_variation_{timestamp}'
    json_out = VARIATION_DIR / f'{prefix}.json'
    md_out = VARIATION_DIR / f'{prefix}.md'

    print('=' * 80)
    print('Full-corpus security-score variation analysis')
    print('=' * 80)
    print(f'workers: {args.workers}')
    print(f'output:  {json_out}')
    print()

    print('Collecting jobs...')
    jobs = collect_jobs()
    if args.limit:
        jobs = jobs[: args.limit]
    print(f'  {len(jobs):,} files to score')
    print()

    print('Initializing worker pool and starting analysis...')
    t0 = time.monotonic()
    results = []
    completed = 0
    with mp.Pool(processes=args.workers, initializer=_worker_init) as pool:
        for r in pool.imap_unordered(_score_file, jobs, chunksize=8):
            results.append(r)
            completed += 1
            if completed % 500 == 0 or completed == len(jobs):
                rate = completed / (time.monotonic() - t0)
                eta = (len(jobs) - completed) / rate if rate > 0 else 0
                print(f'  {completed:>6,}/{len(jobs):,}  '
                      f'({completed/len(jobs)*100:5.1f}%)  '
                      f'{rate:6.1f} files/sec  '
                      f'ETA {eta/60:5.1f} min', flush=True)

    elapsed = time.monotonic() - t0
    print()
    print(f'analysis complete in {elapsed/60:.2f} min')
    print(f'  {len(results):,} attempts, {sum(1 for r in results if r and "error" not in r):,} successful')
    print()

    print('Aggregating...')
    summary, per_model_stats, per_model_groups = aggregate(results)

    out = {
        'timestamp': datetime.now().isoformat(),
        'analysis_duration_seconds': elapsed,
        'total_files_attempted': len(results),
        'workers': args.workers,
        'models_analyzed': sorted(per_model_stats.keys()),
        'summary': summary,
        'per_model_stats': per_model_stats,
        'per_model_groups': {
            m: [{'prompt_id': g['prompt_id'], 'range': g['range'],
                 'mean': g['mean'], 'stdev': g['stdev'],
                 'min': g['min'], 'max': g['max']} for g in groups]
            for m, groups in per_model_groups.items()
        },
    }
    json_out.write_text(json.dumps(out, indent=2))
    print(f'wrote {json_out} ({json_out.stat().st_size / 1024:.1f} KB)')

    # Markdown report
    lines = [
        f'# Full Security Score Variation — {datetime.now().strftime("%Y-%m-%d %H:%M")}',
        '',
        f'Recomputed scores on **all {len(results):,} files** in '
        f'`variation_study/<model>_temp1.0/run<n>/`. Wall-time: '
        f'{elapsed/60:.1f} min on {args.workers} workers.',
        '',
        '## Summary',
        '',
        f'- Prompt groups (model × prompt with ≥3 successful runs): **{summary["total_prompt_groups"]:,}**',
        f'- Average score range across runs: **{summary["avg_range"]:.2f} pp**',
        f'- Median score range: **{summary["median_range"]:.2f} pp**',
        f'- Max score range observed: **{summary["max_range"]:.2f} pp**',
        '',
        '### Variation distribution',
        '',
        f'- Minimal variation (≤1pp): **{summary["minimal_variation_pct"]:.1f}%**',
        f'- Moderate variation (1-5pp): **{summary["moderate_variation_pct"]:.1f}%**',
        f'- Significant variation (>5pp): **{summary["significant_variation_pct"]:.1f}%**',
        f'- Extreme variation (≥90pp): **{summary["extreme_variation_pct"]:.1f}%**',
        '',
        '## Per-model consistency',
        '',
        '| Rank | Model | Consistency (≤1pp) | Avg Range | Max Range | Prompts |',
        '|---|---|---|---|---|---|',
    ]
    ranked = sorted(per_model_stats.items(),
                    key=lambda kv: kv[1]['consistency_rate'], reverse=True)
    for i, (model, st) in enumerate(ranked, 1):
        lines.append(
            f'| {i} | {model} | {st["consistency_rate"]:.1f}% | '
            f'{st["avg_range"]:.2f}pp | {st["max_range"]:.2f}pp | '
            f'{st["num_prompt_groups"]:,} |'
        )
    md_out.write_text('\n'.join(lines))
    print(f'wrote {md_out}')


if __name__ == '__main__':
    main()
