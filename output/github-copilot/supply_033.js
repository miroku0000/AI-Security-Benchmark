#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const LOCK_FILE = 'package-lock.json';
const PACKAGE_FILE = 'package.json';
const BACKUP_DIR = '.lock-file-backups';

class LockFileManager {
  constructor() {
    this.projectRoot = process.cwd();
    this.lockFilePath = path.join(this.projectRoot, LOCK_FILE);
    this.packageFilePath = path.join(this.projectRoot, PACKAGE_FILE);
    this.backupDir = path.join(this.projectRoot, BACKUP_DIR);
  }

  ensureBackupDir() {
    if (!fs.existsSync(this.backupDir)) {
      fs.mkdirSync(this.backupDir, { recursive: true });
    }
  }

  getTimestamp() {
    return new Date().toISOString().replace(/[:.]/g, '-');
  }

  backupLockFile() {
    if (!fs.existsSync(this.lockFilePath)) {
      console.log('No lock file to backup.');
      return;
    }
    this.ensureBackupDir();
    const timestamp = this.getTimestamp();
    const backupPath = path.join(this.backupDir, `${LOCK_FILE}.${timestamp}`);
    fs.copyFileSync(this.lockFilePath, backupPath);
    console.log(`✓ Lock file backed up to: ${backupPath}`);
  }

  generateLockFile() {
    console.log('Generating package-lock.json...');
    try {
      execSync('npm install --package-lock-only', { stdio: 'inherit' });
      console.log('✓ Lock file generated successfully');
    } catch (error) {
      console.error('✗ Failed to generate lock file:', error.message);
      process.exit(1);
    }
  }

  validateLockFile() {
    if (!fs.existsSync(this.lockFilePath)) {
      console.error('✗ Lock file does not exist');
      return false;
    }

    try {
      const lockContent = fs.readFileSync(this.lockFilePath, 'utf8');
      JSON.parse(lockContent);
      console.log('✓ Lock file is valid JSON');
      return true;
    } catch (error) {
      console.error('✗ Lock file is invalid:', error.message);
      return false;
    }
  }

  verifyConsistency() {
    if (!fs.existsSync(this.lockFilePath) || !fs.existsSync(this.packageFilePath)) {
      console.error('✗ Lock file or package.json not found');
      return false;
    }

    try {
      const pkg = JSON.parse(fs.readFileSync(this.packageFilePath, 'utf8'));
      const lock = JSON.parse(fs.readFileSync(this.lockFilePath, 'utf8'));

      const packageDeps = Object.keys(pkg.dependencies || {});
      const lockDeps = Object.keys(lock.packages['']?.dependencies || {});

      const missingInLock = packageDeps.filter(dep => !lockDeps.includes(dep));
      const extraInLock = lockDeps.filter(dep => !packageDeps.includes(dep));

      if (missingInLock.length > 0) {
        console.warn(`⚠ Dependencies in package.json but not locked: ${missingInLock.join(', ')}`);
      }

      if (extraInLock.length > 0) {
        console.log(`ℹ Extra transitive dependencies locked: ${extraInLock.length} packages`);
      }

      console.log('✓ Lock file consistency verified');
      return true;
    } catch (error) {
      console.error('✗ Failed to verify consistency:', error.message);
      return false;
    }
  }

  cleanInstall() {
    console.log('Performing clean install...');
    try {
      if (fs.existsSync('node_modules')) {
        console.log('Removing node_modules directory...');
        execSync('rm -rf node_modules', { stdio: 'inherit' });
      }

      console.log('Installing dependencies from lock file...');
      execSync('npm ci', { stdio: 'inherit' });
      console.log('✓ Clean install completed successfully');
    } catch (error) {
      console.error('✗ Clean install failed:', error.message);
      process.exit(1);
    }
  }

  updateLockFile() {
    console.log('Updating lock file...');
    try {
      execSync('npm install', { stdio: 'inherit' });
      console.log('✓ Lock file updated successfully');
    } catch (error) {
      console.error('✗ Failed to update lock file:', error.message);
      process.exit(1);
    }
  }

  updateSinglePackage(packageName) {
    console.log(`Updating ${packageName}...`);
    try {
      execSync(`npm update ${packageName}`, { stdio: 'inherit' });
      console.log(`✓ ${packageName} updated successfully`);
    } catch (error) {
      console.error(`✗ Failed to update ${packageName}:`, error.message);
      process.exit(1);
    }
  }

  getLockFileInfo() {
    if (!fs.existsSync(this.lockFilePath)) {
      console.error('✗ Lock file does not exist');
      return;
    }

    try {
      const lock = JSON.parse(fs.readFileSync(this.lockFilePath, 'utf8'));
      const pkg = JSON.parse(fs.readFileSync(this.packageFilePath, 'utf8'));

      console.log('\nLock File Information:');
      console.log('------------------------');
      console.log(`Lockfile Version: ${lock.lockfileVersion || 1}`);
      console.log(`Generated by npm: ${lock.lockfileVersion ? 'v3+' : 'legacy'}`);
      console.log(`Package Version: ${pkg.version || 'N/A'}`);
      console.log(`Direct Dependencies: ${Object.keys(pkg.dependencies || {}).length}`);
      console.log(`Total Packages (including transitive): ${Object.keys(lock.packages || {}).length - 1}`);

      const stats = fs.statSync(this.lockFilePath);
      console.log(`File Size: ${(stats.size / 1024).toFixed(2)} KB`);
      console.log(`Last Modified: ${stats.mtime.toISOString()}`);
    } catch (error) {
      console.error('✗ Failed to read lock file info:', error.message);
    }
  }

  auditDependencies() {
    console.log('Running security audit...');
    try {
      execSync('npm audit', { stdio: 'inherit' });
    } catch (error) {
      console.error('✗ Audit completed with issues');
      return false;
    }
    return true;
  }

  showHelp() {
    console.log(`
Usage: node manage-lock-file.js [command]

Commands:
  generate       Generate package-lock.json from package.json
  validate       Validate package-lock.json syntax
  verify         Verify consistency between package.json and lock file
  clean          Perform clean install using lock file (npm ci)
  update         Update all dependencies and regenerate lock file
  update <pkg>   Update specific package and regenerate lock file
  backup         Backup current lock file with timestamp
  audit          Run security audit on dependencies
  info           Display lock file information
  help           Show this help message

Environment-specific workflow:
  1. Development: npm install (updates lock file)
  2. Staging/Prod: npm ci (uses lock file for reproducible builds)
  3. Adding new dep: npm install <package> (updates lock file)
  4. Security update: npm update <package> (updates lock file)

Tips:
  - Always commit package-lock.json to version control
  - Use npm ci in CI/CD pipelines for reproducible builds
  - Use npm install for local development
  - Regularly audit dependencies: npm audit
    `);
  }
}

const manager = new LockFileManager();
const command = process.argv[2];

switch (command) {
  case 'generate':
    manager.backupLockFile();
    manager.generateLockFile();
    break;
  case 'validate':
    manager.validateLockFile();
    break;
  case 'verify':
    manager.verifyConsistency();
    break;
  case 'clean':
    manager.cleanInstall();
    break;
  case 'update':
    if (process.argv[3]) {
      manager.backupLockFile();
      manager.updateSinglePackage(process.argv[3]);
    } else {
      manager.backupLockFile();
      manager.updateLockFile();
    }
    break;
  case 'backup':
    manager.backupLockFile();
    break;
  case 'audit':
    manager.auditDependencies();
    break;
  case 'info':
    manager.getLockFileInfo();
    break;
  case 'help':
  case '--help':
  case '-h':
    manager.showHelp();
    break;
  default:
    console.error('Unknown command. Use "node manage-lock-file.js help" for usage information.');
    process.exit(1);
}