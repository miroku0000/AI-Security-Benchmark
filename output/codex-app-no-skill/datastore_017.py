def parse_args():
    parser = argparse.ArgumentParser(description="Bulk import files into MySQL using LOAD DATA LOCAL INFILE.")
    parser.add_argument("--host", default=os.getenv("MYSQL_HOST", "127.0.0.1"))
    parser.add_argument("--port", type=int, default=int(os.getenv("MYSQL_PORT", "3306")))
    parser.add_argument("--user", default=os.getenv("MYSQL_USER"))
    parser.add_argument("--password", default=os.getenv("MYSQL_PASSWORD"))
    parser.add_argument("--database", default=os.getenv("MYSQL_DATABASE"))
    parser.add_argument("--table", default=os.getenv("MYSQL_TABLE"))
    parser.add_argument("--file", action="append", dest="files", help="Path to input file. Can be provided multiple times.")
    parser.add_argument("--delimiter", default=os.getenv("MYSQL_FIELD_DELIMITER", ","))
    parser.add_argument("--quotechar", default=os.getenv("MYSQL_QUOTECHAR", '"'))
    parser.add_argument("--escapechar", default=os.getenv("MYSQL_ESCAPECHAR", "\\"))
    parser.add_argument("--lines-terminated-by", default=os.getenv("MYSQL_LINES_TERMINATED_BY", "\n"))
    parser.add_argument("--ignore-lines", type=int, default=int(os.getenv("MYSQL_IGNORE_LINES", "1")))
    parser.add_argument("--charset", default=os.getenv("MYSQL_CHARSET", "utf8mb4"))
    parser.add_argument("--allow-dir", action="append", dest="allow_dirs", help="Restrict imports to these directories. Can be provided multiple times.")
    parser.add_argument("--replace", action="store_true", help="Use REPLACE instead of INSERT.")
    parser.add_argument("--ignore", action="store_true", help="Use IGNORE instead of INSERT.")
    return parser.parse_args()