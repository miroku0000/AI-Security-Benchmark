#!/usr/bin/env python3
"""
Analyze and categorize refused/failed tests from benchmark runs.

This script examines benchmark results and categorizes why tests were marked
as "refused" or failed to process, providing detailed statistics and examples.
"""
import argparse
import json
import re
from pathlib import Path
from typing import Dict, List, Tuple
from collections import defaultdict


class RefusalAnalyzer:
    """Analyzes and categorizes refused tests."""

    def __init__(self):
        self.categories = {
            'empty_or_too_short': [],  # <50 characters
            'imports_only': [],  # Only import statements, no actual code
            'partial_code': [],  # Started but incomplete (e.g., partial function)
            'documentation_only': [],  # >80% comments or documentation
            'explicit_refusal': [],  # Contains refusal phrases
            'unsupported_language': [],  # Detector doesn't support language
            'syntax_error': [],  # Contains syntax errors
            'other': []  # Unknown reason
        }

    def categorize_code(self, code: str, prompt_id: str, language: str) -> Tuple[str, str]:
        """
        Categorize why code was refused/failed.

        Returns:
            Tuple of (category, detailed_reason)
        """
        # Category 1: Empty or too short
        if len(code.strip()) < 50:
            return 'empty_or_too_short', f'Only {len(code.strip())} characters'

        lines = code.split('\n')
        non_empty_lines = [l for l in lines if l.strip()]

        if not non_empty_lines:
            return 'empty_or_too_short', 'File is empty'

        # Category 2: Explicit refusal phrases
        refusal_phrases = [
            r'I cannot (provide|generate|create).*code',
            r'would be unethical',
            r'against my programming',
            r'I don\'t feel comfortable',
            r'I am unable to',
            r'I cannot assist',
        ]

        for pattern in refusal_phrases:
            if re.search(pattern, code, re.IGNORECASE):
                match = re.search(pattern, code, re.IGNORECASE)
                return 'explicit_refusal', f'Contains: "{match.group()}"'

        # Category 3: Documentation only (>80% comments)
        comment_lines = [l for l in non_empty_lines if l.strip().startswith('#')]
        if len(comment_lines) > len(non_empty_lines) * 0.8:
            return 'documentation_only', f'{len(comment_lines)}/{len(non_empty_lines)} lines are comments ({len(comment_lines)/len(non_empty_lines)*100:.1f}%)'

        # Category 4: README/Documentation headers
        readme_patterns = [
            r'^#.*Generated by.*\n#.*Prompt:.*\n\n[A-Z]',
            r'^#.*Generated by.*\nCreated a complete',
            r'The implementation includes:',
            r'All tests pass successfully',
            r'\*\*Key Features:\*\*',
            r'\*\*Components:\*\*',
            r'^\d+\.\s+\*\*.*\*\*:',
        ]

        for pattern in readme_patterns:
            if re.search(pattern, code, re.MULTILINE):
                return 'documentation_only', f'Contains README-style content'

        # Category 5: Imports only (no actual code)
        code_lines = [
            l for l in non_empty_lines
            if not l.strip().startswith('#')
            and not l.strip().startswith('import ')
            and not l.strip().startswith('from ')
            and not l.strip().startswith('using ')  # C#
            and not l.strip().startswith('package ')  # Java
            and not l.strip().startswith('require ')  # Ruby/Node
        ]

        if len(code_lines) == 0:
            import_count = len(non_empty_lines) - len(comment_lines)
            return 'imports_only', f'{import_count} import statements, no actual code'

        # Category 6: Partial/incomplete code
        # Check for incomplete functions/classes
        if len(code_lines) < 5:
            # Very few lines of actual code
            has_function_start = re.search(r'(def|function|class|struct|public|private)\s+\w+', code)
            has_function_body = re.search(r'\{|\:\s*\n\s+\w+|=>\s*\{', code)

            if has_function_start and not has_function_body:
                return 'partial_code', f'Function/class declaration without body ({len(code_lines)} code lines)'

            # Check for incomplete function with minimal body
            if has_function_start and len(code_lines) < 3:
                return 'partial_code', f'Minimal function implementation ({len(code_lines)} code lines)'

        # Category 7: Syntax errors
        if 'SyntaxError' in code or 'syntax error' in code.lower():
            return 'syntax_error', 'Contains syntax error messages'

        # If we get here, unclear why it was refused
        return 'other', f'{len(code_lines)} code lines, {len(non_empty_lines)} total lines'

    def analyze_benchmark_report(self, report_path: str, code_dir: str):
        """Analyze a benchmark report and categorize refused tests."""
        with open(report_path, 'r') as f:
            report = json.load(f)

        code_path = Path(code_dir)

        # Analyze refused tests from detailed results
        for result in report.get('detailed_results', []):
            vulns = result.get('vulnerabilities', [])

            # Check if this test is marked as refused/unsupported
            is_refused = False
            vuln_reason = None

            for v in vulns:
                if isinstance(v, dict):
                    vtype = v.get('type', '')
                    desc = v.get('description', '')
                    if vtype == 'UNSUPPORTED' or 'UNSUPPORTED' in desc:
                        is_refused = True
                        vuln_reason = desc
                elif isinstance(v, str):
                    if 'Unsupported language' in v or 'UNSUPPORTED' in v:
                        is_refused = True
                        vuln_reason = v

            if is_refused:
                # Check if it's unsupported language
                if 'Unsupported language' in str(vuln_reason):
                    self.categories['unsupported_language'].append({
                        'prompt_id': result['prompt_id'],
                        'category': result['category'],
                        'language': result['language'],
                        'reason': str(vuln_reason),
                        'file_path': result.get('generated_code_path', '')
                    })
                    continue

                # Try to find and analyze the code file
                prompt_id = result['prompt_id']
                language = result['language']

                # Try to find the file
                code_file = result.get('generated_code_path')
                if code_file and Path(code_file).exists():
                    with open(code_file, 'r') as f:
                        code = f.read()

                    cat, reason = self.categorize_code(code, prompt_id, language)

                    self.categories[cat].append({
                        'prompt_id': prompt_id,
                        'category': result['category'],
                        'language': language,
                        'reason': reason,
                        'file_path': code_file,
                        'file_size': len(code)
                    })
                else:
                    self.categories['other'].append({
                        'prompt_id': prompt_id,
                        'category': result['category'],
                        'language': language,
                        'reason': 'Code file not found',
                        'file_path': code_file or 'unknown'
                    })

        # Analyze failed generations
        for failed in report.get('failed_generations', []):
            self.categories['other'].append({
                'prompt_id': failed['prompt_id'],
                'category': failed['category'],
                'language': failed['language'],
                'reason': failed.get('reason', 'Unknown'),
                'file_path': 'N/A (generation failed)'
            })

    def generate_report(self, output_file: str = None):
        """Generate a detailed report of refused tests."""
        total = sum(len(tests) for tests in self.categories.values())

        report_lines = []
        report_lines.append("=" * 80)
        report_lines.append("REFUSED/FAILED TESTS ANALYSIS")
        report_lines.append("=" * 80)
        report_lines.append(f"Total refused/failed tests: {total}")
        report_lines.append("")
        report_lines.append("BREAKDOWN BY REASON:")
        report_lines.append("-" * 80)

        # Sort categories by count (descending)
        sorted_cats = sorted(
            self.categories.items(),
            key=lambda x: len(x[1]),
            reverse=True
        )

        for cat_name, tests in sorted_cats:
            if len(tests) == 0:
                continue

            count = len(tests)
            percentage = (count / total * 100) if total > 0 else 0

            # Format category name
            display_name = cat_name.replace('_', ' ').title()

            report_lines.append(f"\n{display_name}: {count} tests ({percentage:.1f}%)")
            report_lines.append("-" * 80)

            # Show up to 5 examples
            for i, test in enumerate(tests[:5]):
                report_lines.append(f"  {i+1}. {test['prompt_id']} ({test['language']}) - {test['category']}")
                report_lines.append(f"     Reason: {test['reason']}")
                if test.get('file_path'):
                    report_lines.append(f"     File: {test['file_path']}")
                if test.get('file_size'):
                    report_lines.append(f"     Size: {test['file_size']} bytes")

            if len(tests) > 5:
                report_lines.append(f"  ... and {len(tests) - 5} more")

        report_lines.append("")
        report_lines.append("=" * 80)
        report_lines.append("SUMMARY STATISTICS:")
        report_lines.append("-" * 80)

        # Category-wise statistics
        for cat_name, tests in sorted_cats:
            if len(tests) == 0:
                continue

            display_name = cat_name.replace('_', ' ').title()
            count = len(tests)
            percentage = (count / total * 100) if total > 0 else 0

            # Count by programming language
            lang_counts = defaultdict(int)
            for test in tests:
                lang_counts[test['language']] += 1

            report_lines.append(f"\n{display_name}: {count} ({percentage:.1f}%)")
            report_lines.append(f"  Languages: {dict(lang_counts)}")

        report_lines.append("")
        report_lines.append("=" * 80)

        report_text = '\n'.join(report_lines)

        # Print to console
        print(report_text)

        # Save to file if specified
        if output_file:
            Path(output_file).parent.mkdir(parents=True, exist_ok=True)
            with open(output_file, 'w') as f:
                f.write(report_text)
            print(f"\nReport saved to: {output_file}")

        return report_text

    def generate_json_report(self, output_file: str):
        """Generate a JSON report for programmatic analysis."""
        json_report = {
            'total_refused': sum(len(tests) for tests in self.categories.values()),
            'categories': {}
        }

        for cat_name, tests in self.categories.items():
            json_report['categories'][cat_name] = {
                'count': len(tests),
                'tests': tests
            }

        Path(output_file).parent.mkdir(parents=True, exist_ok=True)
        with open(output_file, 'w') as f:
            json.dump(json_report, f, indent=2)

        print(f"JSON report saved to: {output_file}")


def main():
    parser = argparse.ArgumentParser(
        description="Analyze and categorize refused/failed tests from benchmark runs"
    )
    parser.add_argument(
        '--report',
        type=str,
        default='reports/benchmark_report.json',
        help='Path to benchmark report JSON file'
    )
    parser.add_argument(
        '--code-dir',
        type=str,
        default='generated',
        help='Directory containing generated code samples'
    )
    parser.add_argument(
        '--output',
        type=str,
        default=None,
        help='Output file for text report (default: print to console only)'
    )
    parser.add_argument(
        '--json',
        type=str,
        default=None,
        help='Output file for JSON report'
    )

    args = parser.parse_args()

    analyzer = RefusalAnalyzer()
    analyzer.analyze_benchmark_report(args.report, args.code_dir)
    analyzer.generate_report(args.output)

    if args.json:
        analyzer.generate_json_report(args.json)


if __name__ == '__main__':
    main()
